    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        String oDetails = " ";
        String team_name = iTeams_Names.getSelectedItem().toString();
        String game_name = iGame_Sel.getSelectedItem().toString();
        String team_id = "";
        String game_code = "";
        String ranking = iRanking.getText();
        try {
            ResultSet ass = db.query("select * from team where team_name = \'" +
                    team_name + "\'");
            ResultSet ass1 = db.query("select * from game where game_name = \'" +
                    game_name + "\'");
            ass.next();
            team_id = ass.getString("team_id");
            game_code = ass1.getString("game_code");
        } catch (SQLException ex) {
            Logger.getLogger(InsertRankingDialog.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        StringBuilder sql
                = new StringBuilder("INSERT INTO team_ranked (team_id, "
                        + "game_code, ranking) "
                        + "VALUES (\'");
        sql.append(team_id);
        sql.append("\',\'");
        sql.append(game_code);
        sql.append("\',\'");
        sql.append(ranking);
        sql.append("\')");
        System.out.println( sql.toString() );

        try {
            db.update( sql.toString() );
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Error inserting player.");
            System.out.println( ex.getMessage() );
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

